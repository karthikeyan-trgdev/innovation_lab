<script>
    $(document).ready(function() {
        // Key for localStorage to track if the user has seen the popup
        const POPUP_SEEN_KEY = 'universalFactsPopupSeen';
        
        // --- Code to Auto-Open the Modal on First Entry to Index Page ---
        var currentPath = window.location.pathname;
        var isIndexPage = currentPath.endsWith('/') || currentPath.endsWith('/index.php');
        
        // Check if it is the index page AND the user has NOT seen the popup before
        if (isIndexPage && localStorage.getItem(POPUP_SEEN_KEY) !== 'true') {
            
            // 1. Mark the popup as seen in local storage
            localStorage.setItem(POPUP_SEEN_KEY, 'true');

            // 2. Open the modal with a slight delay
            setTimeout(function() {
                $('#universal_facts').modal('show');
            }, 500); // 500ms delay to ensure page load
        }
        
        // --- Existing Code for Owl Carousel Initialization (moved inside shown.bs.modal) ---
        
        // Cybermax modal initialization logic (runs only when the modal is shown)
        $('#universal_facts').on('shown.bs.modal', function() {
            var $carousel = $(this).find('.owl-carousel');

            // Only initialize if not already initialized (.owl-loaded)
            if (!$carousel.hasClass('owl-loaded')) {
                $carousel.owlCarousel({
                    loop: false,
                    margin: 10,
                    nav: true,
                    dots: false,
                    navText: [
                        '<span class="owl-prev-icon"><i class="fa fa-chevron-left"></i></span>',
                        '<span class="owl-next-icon"><i class="fa fa-chevron-right"></i></span>'
                    ],
                    responsive: {
                        0: {
                            items: 1
                        },
                        600: {
                            items: 1
                        },
                        1000: {
                            items: 1
                        }
                    }
                });
            } else {
                // If it was loaded before (e.g., if the user closed and re-opened it manually in the same session), refresh it
                $carousel.trigger('refresh.owl.carousel');
            }
        });
    });
</script>