<script>
    $(document).ready(function() {
        // Initialize quiz score tracking
        let quizScore = 0;
        let answeredQuestions = 0;
        const totalQuestions = 5;

        // Quiz answer validation
        $('.answer-button').on('click', function() {
            const $clickedButton = $(this);
            const $allButtons = $clickedButton.closest('.quiz-options').find('.answer-button');
            const $correctButton = $allButtons.filter('.correct-answer');

            // Check if this question was already answered
            const alreadyAnswered = $allButtons.prop('disabled');
            if (alreadyAnswered) return;

            // Disable all buttons after selection
            $allButtons.prop('disabled', true);
            answeredQuestions++;

            // Check if clicked button is correct
            if ($clickedButton.hasClass('correct-answer')) {
                quizScore++;
                // Correct answer - turn green
                $clickedButton.css({
                    'background-color': '#28a745',
                    'color': '#ffffff',
                    'border-color': '#28a745'
                });
            } else {
                // Wrong answer - turn clicked button red
                $clickedButton.css({
                    'background-color': '#dc3545',
                    'color': '#ffffff',
                    'border-color': '#dc3545'
                });

                // Show correct answer in green
                $correctButton.css({
                    'background-color': '#28a745',
                    'color': '#ffffff',
                    'border-color': '#28a745'
                });

                // Turn all other wrong answers red (except correct one)
                $allButtons.not('.correct-answer').not($clickedButton).css({
                    'background-color': '#dc3545',
                    'color': '#ffffff',
                    'border-color': '#dc3545',
                    'opacity': '0.6'
                });
            }

            // Check if all questions are answered
            if (answeredQuestions === totalQuestions) {
                setTimeout(function() {
                    showQuizResults();
                }, 1000);
            }
        });

        // Function to show quiz results
        function showQuizResults() {
            const percentage = (quizScore / totalQuestions) * 100;
            let title = '';
            let icon = '';
            let message = '';

            if (percentage === 100) {
                title = 'üéâ Perfect Score!';
                icon = 'success';
                message = `Excellent! You got all ${totalQuestions} questions correct!`;
            } else if (percentage >= 80) {
                title = 'üëè Great Job!';
                icon = 'success';
                message = `You scored ${quizScore} out of ${totalQuestions}. Well done!`;
            } else if (percentage >= 60) {
                title = 'üëç Good Effort!';
                icon = 'info';
                message = `You scored ${quizScore} out of ${totalQuestions}. Keep learning!`;
            } else {
                title = 'üìö Keep Practicing!';
                icon = 'warning';
                message = `You scored ${quizScore} out of ${totalQuestions}. Try again to improve!`;
            }

            Swal.fire({
                title: title,
                html: `
                <div style="font-size: 18px; margin: 20px 0;">
                    ${message}
                </div>
                <div style="font-size: 48px; font-weight: bold; color: #4CAF50; margin: 20px 0;">
                    ${quizScore}/${totalQuestions}
                </div>
                <div style="font-size: 16px; color: #666;">
                    Percentage: ${percentage.toFixed(0)}%
                </div>
            `,
                icon: icon,
                confirmButtonText: 'Try Again',
                confirmButtonColor: '#3085d6',
                allowOutsideClick: false
            }).then((result) => {
                if (result.isConfirmed) {
                    // Reset quiz
                    resetQuiz();
                }
            });
        }

        // Function to reset quiz
        function resetQuiz() {
            quizScore = 0;
            answeredQuestions = 0;

            $('.answer-button').each(function() {
                $(this).prop('disabled', false);
                $(this).css({
                    'background-color': '',
                    'color': '',
                    'border-color': '',
                    'opacity': ''
                });
            });

            // Go back to first question
            var $carousel = $('#myModal').find('.owl-carousel');
            if ($carousel.hasClass('owl-loaded')) {
                $carousel.trigger('to.owl.carousel', [0, 300]);
            }
        }

        // Reset quiz when modal is closed
        $('#myModal').on('hidden.bs.modal', function() {
            resetQuiz();
        });

        // Reset quiz when navigating between carousel items
        $('.owl-carousel').on('changed.owl.carousel', function(event) {
            // Don't reset individual questions, allow navigation while maintaining state
        });

        // Cybermax modal - Disable backdrop click to close
        $('#myModal').modal({
            backdrop: 'static',
            keyboard: false
        });

        $('#myModal').on('shown.bs.modal', function() {
            var $carousel = $(this).find('.owl-carousel');

            if (!$carousel.hasClass('owl-loaded')) {
                $carousel.owlCarousel({
                    loop: false,
                    margin: 10,
                    nav: true,
                    dots: false,
                    navText: [
                        '<span class="owl-prev-icon"><i class="fa fa-chevron-left"></i></span>',
                        '<span class="owl-next-icon"><i class="fa fa-chevron-right"></i></span>'
                    ],
                    responsive: {
                        0: {
                            items: 1
                        },
                        600: {
                            items: 1
                        },
                        1000: {
                            items: 1
                        }
                    }
                });
            } else {
                $carousel.trigger('refresh.owl.carousel');
            }
        });

        // Cosmic Time Lines modal
        $('#cosmic_popup').on('shown.bs.modal', function() {
            var $carousel = $(this).find('.owl-carousel');

            if (!$carousel.hasClass('owl-loaded')) {
                $carousel.owlCarousel({
                    loop: false,
                    margin: 10,
                    nav: true,
                    dots: false,
                    navText: [
                        '<span class="owl-prev-icon"><i class="fa fa-chevron-left"></i></span>',
                        '<span class="owl-next-icon"><i class="fa fa-chevron-right"></i></span>'
                    ],
                    responsive: {
                        0: {
                            items: 1
                        },
                        600: {
                            items: 1
                        },
                        1000: {
                            items: 1
                        }
                    }
                });
            } else {
                $carousel.trigger('refresh.owl.carousel');
            }
        });
    });
</script>